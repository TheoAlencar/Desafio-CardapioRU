package DesafioIncode.DesafioRU;

public class Alimentos {
    // Classe Alimentos representa um alimento
    String nome; // Nome do alimento
    // Construtor para inicializar o alimento
    public Alimentos(String nome) {
        this.nome = nome;
    }

    // Metodo para retornar o nome do alimento
    public String toString() {
        return nome;
    }
}

